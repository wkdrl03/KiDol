package com.web.app.dto;

public class ClubInformBoardDto {
	
    private int boardnumber;
	private String mainarea;
	private String clubpersonnumber;
	private String clubstartdate;
	private String clubname;
	public int getBoardnumber() {
		return boardnumber;
	}
	public void setBoardnumber(int boardnumber) {
		this.boardnumber = boardnumber;
	}
	public String getMainarea() {
		return mainarea;
	}
	public void setMainarea(String mainarea) {
		this.mainarea = mainarea;
	}
	public String getClubpersonnumber() {
		return clubpersonnumber;
	}
	public void setClubpersonnumber(String clubpersonnumber) {
		this.clubpersonnumber = clubpersonnumber;
	}
	public String getClubstartdate() {
		return clubstartdate;
	}
	public void setClubstartdate(String clubstartdate) {
		this.clubstartdate = clubstartdate;
	}
	public String getClubname() {
		return clubname;
	}
	public void setClubname(String clubname) {
		this.clubname = clubname;
	}
	
	
}
